// Der EventListener hört auf das "DOMContentLoaded"-Ereignis, das ausgelöst wird,
// wenn das HTML-Dokument vollständig geladen und das DOM (Document Object Model) verfügbar ist.
document.addEventListener("DOMContentLoaded", function() {
    // Sobald das Dokument vollständig geladen ist, wird dieser Code ausgeführt
    console.log("Script geladen!");  // Gibt eine Nachricht in der Konsole aus, um zu bestätigen, dass das Skript geladen wurde
});